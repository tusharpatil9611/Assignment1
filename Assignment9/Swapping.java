package Assignment9;

public class Swapping {

	public static void main(String[] args) {
		int a=20;
		int b=30;
		int c=a;
		System.out.println("Before Swapping");
		System.out.println(a);
		System.out.println(b);
		a=b;
		b=c;
		c=a;
		System.out.println("After Swapping");
		System.out.println(a);
		System.out.println(b);
		
		
	}

}
